<script>
export default {
  name: 'hello',
  data () {
    return {
      msg: '欢迎来到VUE的世界，这个介绍了各个钩子函数（即：事件）'
    }
  },
  //组件实例刚被创建，组件属性计算之前，如：data属性等。
  beforeCreate: function () {
      console.group('beforeCreate 组件实例刚被创建，组件属性计算之前，如：data属性等===============》');
      console.log("%c%s", "color:red" , "el     : " + this.$el); //undefined
      console.log("%c%s", "color:red","data   : " + this.$data); //undefined 
      console.log("%c%s", "color:red","message: " + this.msg)  
  },
  //组件实例创建完成，组件属性已绑定，但DOM还未生成，$el属性还不存在。
  created: function () {
      console.group('created 组件实例创建完成，组件属性已绑定，但DOM还未生成，$el属性还不存在===============》');
      console.log("%c%s", "color:red","el     : " + this.$el); //undefined
      console.log("%c%s", "color:red","data   : " + this.$data); //已被初始化 
      console.log("%c%s", "color:red","message: " + this.msg); //已被初始化
  },
  //模版编译/挂载之前
  beforeMount: function () {
      console.group('beforeMount 模版编译/挂载之前===============》');
      console.log("%c%s", "color:red","el     : " + (this.$el)); //已被初始化
      console.log(this.$el);
      console.log("%c%s", "color:red","data   : " + this.$data); //已被初始化  
      console.log("%c%s", "color:red","message: " + this.msg); //已被初始化  
  },
  //模版编译/挂载之后（不能保证组件已在docment中）
  mounted: function () {
      console.group('mounted 模版编译/挂载之后===============》');
      console.log("%c%s", "color:red","el     : " + this.$el); //已被初始化
      console.log(this.$el);    
      console.log("%c%s", "color:red","data   : " + this.$data); //已被初始化
      console.log("%c%s", "color:red","message: " + this.msg); //已被初始化 
  },
  //组件更新之前
  beforeUpdate: function () {
      console.group('beforeUpdate 组件更新之前===============》');
      console.log("%c%s", "color:red","el     : " + this.$el);
      console.log(this.$el);   
      console.log("%c%s", "color:red","data   : " + this.$data); 
      console.log("%c%s", "color:red","message: " + this.msg); 
  },
  //组件更新之后
  updated: function () {
      console.group('updated 组件更新之后===============》');
      console.log("%c%s", "color:red","el     : " + this.$el);
      console.log(this.$el); 
      console.log("%c%s", "color:red","data   : " + this.$data); 
      console.log("%c%s", "color:red","message: " + this.msg); 
  },
  //组件销毁之前
  beforeDestroy: function () {
      console.group('beforeDestroy 组件销毁之前===============》');
      console.log("%c%s", "color:red","el     : " + this.$el);
      console.log(this.$el);    
      console.log("%c%s", "color:red","data   : " + this.$data); 
      console.log("%c%s", "color:red","message: " + this.msg); 
  },
  //组件销毁之后
  destroyed: function () {
      console.group('destroyed 组件销毁之后===============》');
      console.log("%c%s", "color:red","el     : " + this.$el);
      console.log(this.$el);  
      console.log("%c%s", "color:red","data   : " + this.$data); 
      console.log("%c%s", "color:red","message: " + this.msg)
  }
}
</script>

